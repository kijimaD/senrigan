// Package timelapse はタイムラプス動画生成機能を提供します。
//
// 主な機能:
// - 映像ソースから定期的にフレームを撮影
// - メモリ上にフレームをバッファリング
// - 定期的に動画を生成・延長
// - 日毎の動画ファイル管理
//
// 責務:
// - TimelapseManager: 全体の管理とスケジューリング
// - TimelapseCapture: 映像ソース毎のキャプチャ処理
// - VideoGenerator: FFmpegを使った動画生成
//
// 仕様:
// - 撮影間隔: デフォルト2秒毎
// - 動画更新間隔: デフォルト1時間毎
// - ファイル分割: 日毎に新しい動画ファイル作成
// - リアルタイム視聴: 作成途中の動画も再生可能
package timelapse
